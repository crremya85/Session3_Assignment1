package Assignment1_Session3;

public class VarClass {   // Class created to explain about Instance Variable

	public static int var1;     // var1 is an Class variable, memory is allocated to the variable

	public int getVar1() { // get method is set to return the value
		return var1;
	}

	public void setVar1(int value) { // set method is to initialize the value to the variable
		var1 = value;
	}
	
	
	
}
