package Assignment1_Session3;

public class VarInstance {  // Class created to explain about Instance Variable

	public int var2; // var1 is an instance variable, no memory allocated now.

	public int getVar2() { // get method is set to return the value
		return var2;
	}

	public void setVar2(int value1) {  // set method is to initialize the value to the variable
		var2 = value1;
	}
	
	
	
}
